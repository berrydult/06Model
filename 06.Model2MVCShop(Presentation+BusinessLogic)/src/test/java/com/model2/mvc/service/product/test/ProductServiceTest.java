	// @Test
	public void testAddProduct() throws Exception {
		Product product = new Product();
		// product.setProdNo(1);
		product.setProdName("물건이름임");
		product.setProdDetail("이거슨 Add 테스트임");
		product.setManuDate("20170101");
		product.setPrice(10000);
		product.setFileName("이미지 없어");

		productService.insertProduct(product);

		product = productService.findProduct(1);

		System.out.println(product.toString());

		// Assert.assertEquals(1, product.getProdNo());
		Assert.assertEquals("물건이름임", product.getProdName());
		Assert.assertEquals("이거슨 Add 테스트임", product.getProdDetail());
		Assert.assertEquals("20170101", product.getManuDate());
		Assert.assertEquals(10000, product.getPrice());
		Assert.assertEquals("이미지 없어", product.getFileName());
	}

	//@Test
	public void testUpdateProduct() throws Exception {

		Product product = productService.findProduct(1);
		Assert.assertNotNull(product);

		Assert.assertEquals("물건이름임", product.getProdName());
		Assert.assertEquals("이거슨 Add 테스트임", product.getProdDetail());
		
		product.setProdName("업데이트물건이름이지");
		product.setProdDetail("이거슨 Update 테스트임");
		
		productService.updateProduct(product);

		product = productService.findProduct(1);
		Assert.assertNotNull(product);

		Assert.assertEquals("업데이트물건이름이지", product.getProdName());
		Assert.assertEquals("이거슨 Update 테스트임", product.getProdDetail());
	}
	
